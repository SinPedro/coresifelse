// guardando cores e simbolos

var cor = prompt("Digite a cor:")

var simbolo = prompt("Digite o símbolo:")



// if e else



if (cor === "vermelho" && simbolo === "X") {
    console.log("CAVE UM BURACO NO CHÃO");
}

else if (cor === "azul" && simbolo === "$"){
    console.log("SIGA EM FRENTE");
}

else if (cor === "verde" && simbolo === "*"){
    console.log("SUBA EM UMA ÁRVORE");
}

else if (cor === "amarelo" && simbolo === "#"){
    console.log("DESCANSEEEE");
}

else {
    console.log("nao há identificação disponível");
}









